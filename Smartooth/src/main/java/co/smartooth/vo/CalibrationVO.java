package co.smartooth.vo;


public class CalibrationVO {
	
	private String userNo;
	private String userId;
	private String calibrationAir;
	private String calibrationKit;
	private String calibrationRef;
	private String macAddress;
	private String calibrationDt;
	
	
	public String getUserNo() {
		return userNo;
	}
	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getCalibrationAir() {
		return calibrationAir;
	}
	public void setCalibrationAir(String calibrationAir) {
		this.calibrationAir = calibrationAir;
	}
	public String getCalibrationKit() {
		return calibrationKit;
	}
	public void setCalibrationKit(String calibrationKit) {
		this.calibrationKit = calibrationKit;
	}
	public String getCalibrationRef() {
		return calibrationRef;
	}
	public void setCalibrationRef(String calibrationRef) {
		this.calibrationRef = calibrationRef;
	}
	public String getMacAddress() {
		return macAddress;
	}
	public void setMacAddress(String macAddress) {
		this.macAddress = macAddress;
	}
	public String getCalibrationDt() {
		return calibrationDt;
	}
	public void setCalibrationDt(String calibrationDt) {
		this.calibrationDt = calibrationDt;
	}
	
}
